#[test]
fn config_from_env() {
    let cfg = Config::from_env();
    // Just verify it doesn't panic and returns sane defaults
    assert!(!cfg.out_dir().as_os_str().is_empty());
    assert!(cfg.array_tuple_limit() > 0);
    assert!(!cfg.file_extension().is_empty());
    assert!(!cfg.large_int().is_empty());
}

#[test]
fn config_builder() {
    let cfg = Config::new()
        .with_out_dir("/tmp/test")
        .with_array_tuple_limit(32)
        .with_file_extension("mjs.flow")
        .with_large_int("number");
    assert_eq!(cfg.out_dir().to_str().unwrap(), "/tmp/test");
    assert_eq!(cfg.array_tuple_limit(), 32);
    assert_eq!(cfg.file_extension(), "mjs.flow");
    assert_eq!(cfg.large_int(), "number");
}

