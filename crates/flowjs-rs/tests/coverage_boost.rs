//! Tests specifically targeting uncovered code paths for coverage.

#![allow(dead_code)]

use flowjs_rs::{Config, Flow};
use std::collections::{BTreeMap, BTreeSet, HashMap, HashSet, LinkedList, VecDeque};

// ── Export system coverage ──────────────────────────────────────────────

#[derive(Flow)]
struct ExportTarget {
    name: String,
    count: i32,
}

#[derive(Flow)]
struct ExportWithDeps {
    target: ExportTarget,
    tags: Vec<String>,
}

#[test]
fn export_to_string_includes_header_and_imports() {
    let cfg = Config::new();
    let content = ExportWithDeps::export_to_string(&cfg).unwrap();
    assert!(content.starts_with("// @flow"));
    assert!(content.contains("Generated by flowjs-rs"));
    assert!(content.contains("import type"));
    assert!(content.contains("ExportTarget"));
    assert!(content.contains("export type ExportWithDeps"));
}

#[test]
fn export_to_string_no_self_import() {
    let cfg = Config::new();
    let content = ExportTarget::export_to_string(&cfg).unwrap();
    assert!(!content.contains("import"), "should not import itself");
}

#[test]
fn export_all_writes_files() {
    let dir = std::env::temp_dir().join("flowjs_coverage_test");
    let _ = std::fs::remove_dir_all(&dir);
    let cfg = Config::new().with_out_dir(&dir);
    ExportWithDeps::export_all(&cfg).unwrap();

    // Both the type and its dependency should be exported
    assert!(dir.join("ExportWithDeps.js.flow").exists());
    assert!(dir.join("ExportTarget.js.flow").exists());

    // Check content
    let content = std::fs::read_to_string(dir.join("ExportWithDeps.js.flow")).unwrap();
    assert!(content.contains("export type ExportWithDeps"));

    let _ = std::fs::remove_dir_all(&dir);
}

#[test]
fn export_single_type() {
    let dir = std::env::temp_dir().join(format!("flowjs_cov_single_{}", std::process::id()));
    let _ = std::fs::remove_dir_all(&dir);
    let cfg = Config::new().with_out_dir(&dir);
    ExportTarget::export(&cfg).unwrap();
    assert!(dir.join("ExportTarget.js.flow").exists());
    let _ = std::fs::remove_dir_all(&dir);
}

#[test]
fn export_idempotent() {
    let dir = std::env::temp_dir().join(format!("flowjs_cov_idempotent_{}", std::process::id()));
    let _ = std::fs::remove_dir_all(&dir);
    let cfg = Config::new().with_out_dir(&dir);
    ExportTarget::export(&cfg).unwrap();
    ExportTarget::export(&cfg).unwrap();
    let _ = std::fs::remove_dir_all(&dir);
}

