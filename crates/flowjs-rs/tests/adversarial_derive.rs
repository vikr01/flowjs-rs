// ═══════════════════════════════════════════════════════════════════════════
// 3. Generic enum with generic fields (custom Result-like type)
//
//    Expected Flow (externally tagged, the default):
//      type MyResult<T, E> = {| Success: T |} | {| Failure: E |};
//
//    VERDICT: WORKS. The macro generates dummy types TDummy/EDummy for decl(),
//    producing: type MyResult<T, E> = {| Success: T |} | {| Failure: E |};
//    For concrete usage e.g. MyResult<String, i32>, name() returns
//    "MyResult<string, number>".
// ═══════════════════════════════════════════════════════════════════════════
#[derive(Flow)]
enum MyResult<T, E> {
    Success(T),
    Failure(E),
}

#[test]
fn generic_enum_with_generic_fields() {
    let c = cfg();
    // Generic decl: uses placeholder type params
    let decl = MyResult::<String, i32>::decl(&c);
    assert!(decl.contains("type MyResult<T, E>"), "{decl}");
    assert!(decl.contains("Success: T"), "{decl}");
    assert!(decl.contains("Failure: E"), "{decl}");

    // Concrete name
    let name = <MyResult<String, i32> as Flow>::name(&c);
    assert_eq!(name, "MyResult<string, number>", "{name}");

    // Concrete decl
    let concrete = <MyResult<String, i32> as Flow>::decl_concrete(&c);
    assert!(concrete.contains("MyResult<string, number>"), "{concrete}");
    assert!(concrete.contains("Success: string"), "{concrete}");
    assert!(concrete.contains("Failure: number"), "{concrete}");
}

// ═══════════════════════════════════════════════════════════════════════════
// 4. Struct with ALL attribute combinations simultaneously
//
//    Expected Flow:
//      type KitchenSink = {|
//        +userId: string,
//        +overridden: mixed,
//      |};
//
//    Attributes exercised: rename, rename_all, export_to, bound, optional_fields,
//    field-level: rename, type override, skip, optional, inline, flatten.
//
//    VERDICT: WORKS (mostly). The container `rename_all` applies to un-renamed
//    fields. Field-level `rename` overrides `rename_all`. `skip` removes the
//    field. `type = "..."` overrides the Flow type. `flatten` merges fields.
//    `inline` expands the type inline. `optional` makes the key optional.
//    The `bound` attribute adds extra where clauses.
//
//    Note: `export_to` only affects the output_path(), not the declaration text.
//    The `optional_fields` on container makes all Option<T> fields use `field?:`.
// ═══════════════════════════════════════════════════════════════════════════
#[derive(Flow)]
struct FlatBase {
    base_field: i32,
}

#[derive(Flow)]
#[flow(rename = "KitchenSink", rename_all = "camelCase", optional_fields)]
struct AllAttributes {
    user_id: String,
    #[flow(rename = "overridden")]
    some_field: bool,
    #[flow(skip)]
    internal_only: Vec<u8>,
    #[flow(type = "mixed")]
    dynamic_data: String,
    #[flow(optional)]
    maybe_present: Option<i32>,
    #[flow(flatten)]
    base: FlatBase,
}

#[test]
fn all_attribute_combinations() {
    let c = cfg();
    let decl = AllAttributes::decl(&c);
    assert!(decl.contains("type KitchenSink"), "{decl}");
    // rename_all = camelCase applies to user_id → userId
    // optional_fields makes ALL fields key-optional (field?:)
    assert!(decl.contains("userId?: string"), "{decl}");
    // field rename overrides rename_all
    assert!(decl.contains("overridden?: boolean"), "{decl}");
    // skip removes internal_only
    assert!(!decl.contains("internalOnly"), "{decl}");
    assert!(!decl.contains("internal_only"), "{decl}");
    // type = "mixed" overrides
    assert!(decl.contains("dynamicData?: mixed"), "{decl}");
    // optional + optional_fields on Option<i32> → maybePresent?: ?number
    assert!(decl.contains("maybePresent?: ?number"), "{decl}");
    // flatten merges FlatBase's fields — note: flattened fields do NOT
    // inherit the parent's optional_fields, since flatten uses
    // inline_flattened which is pre-computed from FlatBase's own attrs.
    assert!(decl.contains("base_field: number"), "{decl}");
}

