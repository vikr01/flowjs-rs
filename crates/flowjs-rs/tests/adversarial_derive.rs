//! Adversarial stress tests for the Flow derive macro.
//!
//! Each test case exercises an edge case or combination of features designed to
//! surface codegen bugs. Every type here SHOULD compile and produce valid Flow.

#![allow(dead_code)]

use flowjs_rs::{Config, Flow};
use std::collections::HashMap;

fn cfg() -> Config {
    Config::new()
}

// ═══════════════════════════════════════════════════════════════════════════
// 1. Deeply nested generics
//    Vec<HashMap<String, Option<Vec<(i32, bool)>>>>
//
//    Expected Flow:
//      type DeeplyNested = {|
//        +data: $ReadOnlyArray<{ [key: string]: ?$ReadOnlyArray<[number, boolean]> }>,
//      |};
//
//    VERDICT: WORKS. Every layer has a blanket impl — Vec, HashMap, Option,
//    tuples — so the derive macro just calls <T>::name(cfg) recursively.
//    The macro itself does not inspect the field type; it delegates to the
//    trait impls which are all provided in impls.rs.
// ═══════════════════════════════════════════════════════════════════════════
#[derive(Flow)]
struct DeeplyNested {
    data: Vec<HashMap<String, Option<Vec<(i32, bool)>>>>,
}

#[test]
fn deeply_nested_generics() {
    let c = cfg();
    let decl = DeeplyNested::decl(&c);
    assert!(decl.contains("type DeeplyNested"), "{decl}");
    // Vec → $ReadOnlyArray
    assert!(decl.contains("$ReadOnlyArray<"), "{decl}");
    // HashMap → { [key: ...]: ... }
    assert!(decl.contains("[key: string]"), "{decl}");
    // Option<Vec<...>> → ?$ReadOnlyArray<...>
    assert!(decl.contains("?$ReadOnlyArray<"), "{decl}");
    // (i32, bool) → [number, boolean]
    assert!(decl.contains("[number, boolean]"), "{decl}");
}

// ═══════════════════════════════════════════════════════════════════════════
